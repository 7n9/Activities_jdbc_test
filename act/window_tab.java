    private void button_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_removeActionPerformed
        List<String> times = new ArrayList<String>();
        List<String> activv = new ArrayList<String>();


        for(int i=0;i<table.getModel().getRowCount();i++)
        {
            if (table.getModel().getValueAt(i,0) != null && (Boolean) table.getModel().getValueAt(i,0))
            {
                times.add(table.getModel().getValueAt(i,1).toString());
                activv.add(table.getModel().getValueAt(i,2).toString());

            }
        }

        for (int j = 0; j < times.size(); j++) {
            try {
                String usuwanie = "DELETE FROM ACTIVITIES WHERE TIMEVAL = '" + times.get(j) + "' AND ACTIVITY = '" + activv.get(j) + "'";
                statement.executeQuery(usuwanie);
            } catch (SQLException ex) {
                ex.printStackTrace();
            }
        }
        
        for(int i=0;i<table.getModel().getRowCount();i++)
        {
            for(int j=0;j<table.getModel().getColumnCount();j++){
                table.setValueAt(null, i, j);
            }
        }
        refreshTab();
    }//GEN-LAST:event_button_removeActionPerformed

