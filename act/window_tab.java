    public void refreshTab(){
    
        try{       
            String zapytanie= "Select TIMEVAL, ACTIVITY FROM ACTIVITIES ORDER BY TIMEVAL ASC";
            ResultSet rs = statement.executeQuery(zapytanie);

            int kolumna=1;
            for (int i=0;rs.next();i++){
            String czas = rs.getString("TIMEVAL");
            table.getModel().setValueAt(czas.substring(0, czas.length()-3), i, kolumna);
            table.getModel().setValueAt(rs.getString("ACTIVITY"), i, kolumna+1);
            }
            table.repaint();

        } catch (SQLException e) {
            e.printStackTrace();
        }
    
    }
    
    
    
